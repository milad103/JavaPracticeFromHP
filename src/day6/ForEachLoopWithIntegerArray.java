package day6;

public class ForEachLoopWithIntegerArray {

        public static void main(String[] args) {
            //Declaring an array
            int arr[]={5,9,17,18,48, 55, 101, 9099};
            //Printing array using for-each loop
            for(int i:arr){
                System.out.println(i);
            }
        }
    }